# Read en.po
with open('en.po', 'r', encoding='utf-8') as f:
    content = f.read()

# Parse msgid/msgstr pairs
entries = []
i = 0
lines = content.split('\n')

while i < len(lines):
    line = lines[i]

    # Skip header msgid ""
    if line.startswith('msgid ""'):
        # Capture the header block
        header_lines = [line]
        i += 1
        while i < len(lines) and lines[i]:
            header_lines.append(lines[i])
            i += 1
        entries.append(('header', header_lines))
        continue

    # Regular msgid entry
    if line.startswith('msgid "'):
        msgid_line = line
        msgid_content = line[7:-1]  # Extract content between msgid " and final "
        i += 1

        # Get msgstr line
        if i < len(lines) and lines[i].startswith('msgstr "'):
            msgstr_line = lines[i]
            msgstr_content = lines[i][7:-1]
            i += 1
        else:
            msgstr_line = None
            msgstr_content = ""

        # Collect any additional msgstr lines (for plural forms)
        while i < len(lines) and lines[i] and not lines[i].startswith('msgid ') and not lines[i].startswith('#'):
            if lines[i].startswith('msgstr '):
                msgstr_line = lines[i]
                msgstr_content = lines[i][7:-1]
            else:
                msgid_content += lines[i]
            i += 1

        entries.append(('entry', msgid_content, msgstr_content))
    else:
        i += 1

# Apply translations
filled = 0
missing = 0

output_lines = []
for entry in entries:
    if entry[0] == 'header':
        # Modify header for Indonesian
        for line in entry[1]:
            if line.startswith('"Language: '):
                output_lines.append('"Language: id-ID\\n"')
            elif line.startswith('"Plural-Forms: '):
                output_lines.append('"Plural-Forms: nplurals=2; plural=(n != 1);\\n"')
            else:
                output_lines.append(line)
    elif entry[0] == 'entry':
        msgid = entry[1]
        msgstr = entry[2]

        if msgid in translations:
            output_lines.append(f'msgid "{msgid}"')
            output_lines.append(f'msgstr "{translations[msgid]}"')
            filled += 1
        else:
            output_lines.append(f'msgid "{msgid}"')
            output_lines.append(f'msgstr "{msgstr}"')  # Keep English if no translation
            missing += 1
        output_lines.append('')

print(f"Filled: {filled}")
print(f"Missing (kept English): {missing}")

# Write output
with open('id-ID.po', 'w', encoding='utf-8') as f:
    f.write('\n'.join(output_lines))

